    public void popularListaArray()
    {
        try
        {
            FileInputStream file = new FileInputStream("Contas.dat");
            ObjectInputStream inputFile = new ObjectInputStream(file);
            
            boolean endOfFile = false;
            
            while(!endOfFile)
            {
                try
                {
                    conta.add((Pessoa) inputFile.readObject());
                }
                catch(EOFException e)
                {
                    endOfFile = true;
                }
                catch(Exception f)
                {
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            
            inputFile.close();
            
        }
        catch(IOException e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }

    
    public void popularListArrayBackup()
    {
        try
        {
            FileInputStream file2 = new FileInputStream("Backups.dat");
            ObjectInputStream inputFile2 = new ObjectInputStream(file2);
            
            boolean endOfFile = false;
            
            while(!endOfFile)
            {
                try
                {
                    backups.add((Backup) inputFile2.readObject());
                }
                catch(EOFException e)
                {
                    endOfFile = true;
                }
                catch(Exception f)
                {
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            
            inputFile2.close();
            
        }
        catch(IOException e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }
    
    public void salvarBackups()
    {
        try
        {
            FileOutputStream file2 = new FileOutputStream("Backups.dat");
            ObjectOutputStream outputFile2 = new ObjectOutputStream(file2);
            
            for(int i = 0 ; i < backups.size() ; i++)
            {
                outputFile2.writeObject(backups.get(i));
            }
            
            outputFile2.close();
            
            JOptionPane.showMessageDialog(null, "Backup realizado com sucesso!"); 
            this.dispose();
        }
        catch(IOException e)
        {
             JOptionPane.showMessageDialog(null, e.getMessage());       
        }
    }
            
    
